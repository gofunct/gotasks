func WithServeFunc(port string) With {
	return func(c *Commander) {
		var err error
		pair, err := tls.X509KeyPair([]byte(Cert), []byte(Key))
		if err != nil {
			panic(err)
		}
		demoKeyPair = &pair
		demoCertPool = x509.NewCertPool()
		ok := demoCertPool.AppendCertsFromPEM([]byte(Cert))
		if !ok {
			panic("bad certs")
		}
		demoAddr = fmt.Sprintf("localhost", port)

		c.RootCmd.AddCommand(&cobra.Command{
			Use:   "serve",
			Short: "Launches the webserver on https://localhost" + port,
			Run: func(c *cobra.Command, args []string) {
				opts := []grpc.ServerOption{
					grpc.Creds(credentials.NewClientTLSFromCert(demoCertPool, "localhost"+port))}

				grpcServer := grpc.NewServer(opts...)
				pb.RegisterEchoServiceServer(grpcServer, newServer())
				ctx := context.Background()

				dcreds := credentials.NewTLS(&tls.Config{
					ServerName: demoAddr,
					RootCAs:    demoCertPool,
				})
				dopts := []grpc.DialOption{grpc.WithTransportCredentials(dcreds)}

				mux := http.NewServeMux()
				mux.HandleFunc("/swagger.json", func(w http.ResponseWriter, req *http.Request) {
					io.Copy(w, strings.NewReader(pb.Swagger))
				})

				gwmux := runtime.NewServeMux()
				err := pb.RegisterEchoServiceHandlerFromEndpoint(ctx, gwmux, demoAddr, dopts)
				if err != nil {
					fmt.Printf("serve: %v\n", err)
					return
				}

				mux.Handle("/", gwmux)
				serveSwagger(mux)

				conn, err := net.Listen("tcp", port)
				if err != nil {
					panic(err)
				}

				srv := &http.Server{
					Addr:    demoAddr,
					Handler: grpcHandlerFunc(grpcServer, mux),
					TLSConfig: &tls.Config{
						Certificates: []tls.Certificate{*demoKeyPair},
						NextProtos:   []string{"h2"},
					},
				}

				fmt.Printf("grpc on port: %d\n", port)
				err = srv.Serve(tls.NewListener(conn, srv.TLSConfig))

				if err != nil {
					log.Fatal("ListenAndServe: ", err)
				}

				return
			}})
	}
}

type myService struct{}

func (m *myService) Echo(c context.Context, s *pb.EchoMessage) (*pb.EchoMessage, error) {
	fmt.Printf("rpc request Echo(%q)\n", s.Value)
	return s, nil
}

func newServer() *myService {
	return new(myService)
}

// grpcHandlerFunc returns an http.Handler that delegates to grpcServer on incoming gRPC
// connections or otherHandler otherwise. Copied from cockroachdb.
func grpcHandlerFunc(grpcServer *grpc.Server, otherHandler http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		// TODO(tamird): point to merged gRPC code rather than a PR.
		// This is a partial recreation of gRPC's internal checks https://github.com/grpc/grpc-go/pull/514/files#diff-95e9a25b738459a2d3030e1e6fa2a718R61
		if r.ProtoMajor == 2 && strings.Contains(r.Header.Get("Content-Type"), "application/grpc") {
			grpcServer.ServeHTTP(w, r)
		} else {
			otherHandler.ServeHTTP(w, r)
		}
	})
}

func serveSwagger(mux *http.ServeMux) {
	mime.AddExtensionType(".svg", "image/svg+xml")

	// Expose files in third_party/swagger-ui/ on <host>/swagger-ui
	fileServer := http.FileServer(&assetfs.AssetFS{
		Asset:    swagger.Asset,
		AssetDir: swagger.AssetDir,
		Prefix:   "third_party/swagger-ui",
	})
	prefix := "/swagger-ui/"
	mux.Handle(prefix, http.StripPrefix(prefix, fileServer))
}
